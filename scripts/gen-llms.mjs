// Normalize the data model: accept either the in-memory `elements` array from
// gen-element-api.mjs OR the dist/custom-elements.json declarations.
// ---------------------------------------------------------------------------
function fromElements(elements) {
  return elements.map((el) => ({
    tag: el.tag,
    reactName: el.displayName ?? tagToReact(el.tag).replace(/^Kai/, ''),
    props: el.props.map((p) => ({
      name: p.name,
      type: p.type,
      scalar: p.scalar,
      description: p.description,
    })),
    events: el.events.map((e) => ({
      name: e.name,
      detail: e.detail ? `CustomEvent<${e.detail}>` : 'CustomEvent',
      description: e.description,
    })),
  }));
}

function fromManifest(cem) {
  const decls = cem.modules[0].declarations;
  return decls.map((d) => {
    const attrByField = new Map(
      (d.attributes || []).filter((a) => a.fieldName).map((a) => [a.fieldName, a]),
    );
    return {
      tag: d.tagName,
      reactName: tagToReact(d.tagName).replace(/^Kai/, ''),
      props: (d.members || [])
        .filter((m) => m.kind === 'field')
        .map((m) => ({
          name: m.name,
          type: m.type?.text ?? 'unknown',
          scalar: attrByField.has(m.name) || isScalarType(m.type?.text ?? ''),
          description: m.description ?? '',
        })),
      events: (d.events || []).map((e) => ({
        name: e.name,
        detail: e.type?.text ?? 'CustomEvent',
        description: e.description ?? '',
      })),
    };
  });
}

export function generate(elementsInput) {
  const els = elementsInput ? fromElements(elementsInput) : fromManifest(
    JSON.parse(readFileSync(resolve(root, 'dist/custom-elements.json'), 'utf8')),
  );
  els.sort((a, b) => a.tag.localeCompare(b.tag));
  const count = els.length;

  const llmsBody = buildLlmsBody(count);
  const llmsTxt = `${HEADER_NOTE}\n${llmsBody}\n`;

  const elementSection = els.map(renderElement).join('\n\n');
  const llmsFull = [
    HEADER_NOTE,
    '# @kitn.ai/ui — Full Reference',
    '',
    llmsBody,
    '',
    '---',
    '',
    RUNBOOK,
    '',
    '---',
    '',
    STREAMING_RECIPE,
    '',
    '---',
    '',
    `## Element reference (${count} elements, generated from custom-elements.json)`,
    '',
    'Every element also accepts the `theme` attribute. Array/object properties are marked with a `—` attribute: they must be set as JS properties.',
    '',
    elementSection,
    '',
  ].join('\n') + '\n';

  mkdirSync(resolve(root, 'dist/llms'), { recursive: true });
  writeFileSync(resolve(root, 'llms.txt'), llmsTxt);
  writeFileSync(resolve(root, 'llms-full.txt'), llmsFull);
  writeFileSync(resolve(root, 'dist/llms/llms.txt'), llmsTxt);
  writeFileSync(resolve(root, 'dist/llms/llms-full.txt'), llmsFull);

  console.log(`✓ llms.txt + llms-full.txt written (${count} elements)`);
  return count;
}

