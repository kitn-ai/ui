// Generates `llms.txt` (curated orientation, llmstxt.org convention) and
// `llms-full.txt` (full per-element API reference) for AI coding agents.
//
// Source of truth = the same `elements` model that `gen-element-api.mjs` uses
// to write dist/custom-elements.json, so these files can never drift from the
// shipped API. When run standalone (no in-memory import) it falls back to
// reading dist/custom-elements.json.
//
// Outputs (all deterministic — no timestamps/random):
//   - llms.txt              (repo root, committed + npm `files`)
//   - llms-full.txt         (repo root, committed + npm `files`)
//   - dist/llms/llms.txt    (copy, shipped in the npm package)
//   - dist/llms/llms-full.txt

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

const toAttr = (name) => name.replace(/([A-Z])/g, '-$1').toLowerCase();
const tagToReact = (tag) =>
  tag.split('-').map((s) => s[0].toUpperCase() + s.slice(1)).join('');
const escapeCell = (s) => String(s ?? '').replace(/\|/g, '\\|').replace(/\n/g, ' ').trim();
const isScalarType = (t) =>
  /^(undefined\s*\|\s*)?('[^']*'(\s*\|\s*'[^']*')*|string|number|boolean|true|false)(\s*\|\s*undefined)?$/.test(
    t.replace(/\s+/g, ' ').trim(),
  );

const HEADER_NOTE = '<!-- AUTO-GENERATED by scripts/gen-llms.mjs — do not edit by hand. Run `npm run build`. -->';

