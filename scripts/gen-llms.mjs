function renderElement(el) {
  const react = el.reactName;
  const propRows = el.props
    .map((p) => {
      const attr = p.scalar ? `\`${toAttr(p.name)}\`` : '—';
      return `| \`${p.name}\` | ${attr} | \`${escapeCell(p.type)}\` | ${escapeCell(p.description)} |`;
    })
    .join('\n');

  const eventRows = el.events
    .map((e) => `| \`${e.name}\` | \`${escapeCell(e.detail || 'CustomEvent')}\` | ${escapeCell(e.description)} |`)
    .join('\n');

  const out = [`### \`${el.tag}\` / \`${react}\``, ''];

  out.push(
    propRows
      ? [
          '**Properties** (every element also accepts `theme="light|dark|auto"`; only scalar props work as HTML attributes):',
          '',
          '| Property | Attribute | Type | Description |',
          '|---|---|---|---|',
          propRows,
        ].join('\n')
      : '_No public properties._',
  );
  out.push('');
  out.push(
    eventRows
      ? [
          '**Events** (non-bubbling `CustomEvent`s — listen directly on the element):',
          '',
          '| Event | `detail` type | Description |',
          '|---|---|---|',
          eventRows,
        ].join('\n')
      : '_No events._',
  );
  out.push('');
  out.push('---');
  return out.join('\n');
}

