// Emits react/index.tsx — typed React wrappers for every element — from the
// extracted metadata. Each wrapper renders the custom element and bridges props
// (DOM properties) + on<Event> handlers (CustomEvent listeners) via react/runtime.

import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const clean = (type, optional) => {
  let t = type
    .replace(/\bUint8Array<ArrayBufferLike>/g, 'Uint8Array')
    .replace(/\bfalse \| true\b/g, 'boolean')
    .replace(/\btrue \| false\b/g, 'boolean');
  if (optional) t = t.replace(/undefined \| /g, '').replace(/ \| undefined/g, '');
  return t.trim();
};

// IMPORTS values are relative to src/elements; rebase to frameworks/react/.
const reactImportPath = (srcElementsRel) =>
  srcElementsRel.startsWith('./')
    ? `../../src/elements/${srcElementsRel.slice(2)}`
    : `../../src/${srcElementsRel.slice(3)}`;

// Event names are lower-kebab, kai-prefixed (e.g. `kai-message-action`). The React/Solid
// handler prop strips the `kai-` prefix and PascalCases on hyphens → `onMessageAction`.
const onName = (ev) => 'on' + ev.replace(/^kai-/, '').split('-').map((s) => s.charAt(0).toUpperCase() + s.slice(1)).join('');

export function writeReact(root, elements, IMPORTS) {
  const used = new Set();
  const scan = (s) => { for (const n of Object.keys(IMPORTS)) if (new RegExp(`\\b${n}\\b`).test(s)) used.add(n); };
  for (const el of elements) {
    for (const p of el.props) scan(p.type);
    for (const e of el.events) if (e.detail) scan(e.detail);
  }
  const bySource = {};
  for (const n of used) (bySource[reactImportPath(IMPORTS[n])] ??= []).push(n);
  const importLines = Object.entries(bySource)
    .map(([src, names]) => `import type { ${names.sort().join(', ')} } from '${src}';`)
    .join('\n');

  const blocks = elements.map((el) => {
    const propLines = el.props.flatMap((p) => [
      ...(p.description ? [`  /** ${p.description} */`] : []),
      `  ${p.name}${p.optional ? '?' : ''}: ${clean(p.type, p.optional)};`,
    ]);
    const eventLines = el.events.flatMap((e) => [
      ...(e.description ? [`  /** ${e.description} */`] : []),
      `  ${onName(e.name)}?: (event: CustomEvent${e.detail ? `<${clean(e.detail, false)}>` : ''}) => void;`,
    ]);
    const propNames = JSON.stringify(['theme', ...el.props.map((p) => p.name)]);
    const eventMap = `{ ${el.events.map((e) => `${onName(e.name)}: '${e.name}'`).join(', ')} }`;

    const name = el.displayName;
    const propsName = `${name}Props`;
    // Lazy-import the element by its SOURCE-MODULE basename (set in gen-element-api.mjs),
    // not its tag: the per-element build emits dist/elements/<source-file>.js, so deriving
    // from the tag (e.g. kai-confirm → "confirm") points at a file that does not exist.
    const moduleName = el.module ?? el.tag.replace(/^kai-/, '');
    return `export interface ${propsName} extends WebComponentProps {
${[...propLines, ...eventLines].join('\n')}
}

export const ${name} = createWebComponent<${propsName}>(
  '${el.tag}',
  ${propNames},
  ${eventMap},
  () => import('@kitn.ai/ui/elements/${moduleName}'),
);`;
  }).join('\n\n');

  const out = `'use client';
// AUTO-GENERATED by scripts/gen-element-api.mjs — do not edit by hand.
// Typed React wrappers for every kitn custom element. Usage:
//   import { Message } from '@kitn.ai/ui/react';
//   <Message message={msg} onMessageAction={(e) => …} />
// Each wrapper lazy-registers ITS element on first client mount (a dynamic import
// of '@kitn.ai/ui/elements/<name>'), so a consumer ships only the elements they use
// — not the register-all bundle. SSR-safe: registration fires only in a client effect.
// For eager all-registration call registerAll() or import '@kitn.ai/ui/elements'.
import { createWebComponent, registerAll, type WebComponentProps } from './runtime';
export { registerAll };
export { useKaiChat } from './use-kai-chat';
export type { UseKaiChatOptions, KaiChatController, ChatMessage } from './use-kai-chat';
${importLines}

${blocks}
`;
  writeFileSync(resolve(root, 'frameworks/react/index.tsx'), out);
  console.log(`✓ frameworks/react/index.tsx — ${elements.length} wrappers`);
}
